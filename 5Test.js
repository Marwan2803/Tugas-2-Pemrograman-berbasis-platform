// Harga telur per kilogram
let hargaPerKilo = 28000;

// Jumlah telur yang dibeli dalam kilogram
let jumlahTelur = 2.5;

// Total uang yang dibayarkan
let totalBayar = 70000;

// Menghitung total harga telur yang dibeli
let totalHarga = hargaPerKilo * jumlahTelur;

// Menghitung uang kembalian
let uangKembalian = totalBayar - totalHarga;

// Menampilkan hasil sesuai format yang diminta
console.log("Jumlah telur (kg): " + jumlahTelur + " kilo");
console.log("Total bayar: Rp " + totalBayar);
console.log("Uang kembalian: Rp " + uangKembalian);
